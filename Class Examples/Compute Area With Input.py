#Description: Computing the area of a circle with user input
'''
We can use the input() function to ask the user to input a value for the radius

Syntax: variable = input()

Note/warning: The input function only reads data in as a string
'''
#Prompt the user to enter a value for the radius
radius = float(input("Enter a number for radius: "))

'''
String_data = input("Enter a number for radius: ") String data type
radius = float(string_data)
'''

#Compute the area
area = radius * radius * 3.14159

#Display the results
print("The area for the circle is", area)




























































































































