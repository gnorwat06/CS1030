#Description: compute the area of a circle
'''
Example:
Step 1: Get the circles radius from the user
Step 2: Compute the area applying the formula
        area = radius * radius * 3.14159
Step 3: Display the results
'''

#Create a variable
#Note: A variable is a name that references a value stored in memory
radius = 20

#Note: Python determines the data type by the value you assign

#Step 2
area = radius * radius * 3.14159

#step 3
print("The area for the circle of radius", radius, "is", area)
